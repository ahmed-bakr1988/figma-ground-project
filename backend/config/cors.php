<?php

/**
 * ================================
 * إعدادات CORS
 * ================================
 * 
 * هذه الإعدادات تتحكم في سياسة الوصول عبر النطاقات
 * للسماح للـ React Frontend بالتواصل مع الـ Laravel API
 */

return [
    /*
    |--------------------------------------------------------------------------
    | المسارات المسموح لها بالـ CORS
    |--------------------------------------------------------------------------
    |
    | المسارات التي سيتم تطبيق إعدادات CORS عليها
    |
    */
    'paths' => [
        'api/*',
        'sanctum/csrf-cookie',
        'login',
        'logout',
        'register',
    ],

    /*
    |--------------------------------------------------------------------------
    | طرق HTTP المسموحة
    |--------------------------------------------------------------------------
    */
    'allowed_methods' => ['*'],

    /*
    |--------------------------------------------------------------------------
    | Origins المسموحة
    |--------------------------------------------------------------------------
    |
    | النطاقات المسموح لها بالوصول للـ API
    | يمكن إضافة نطاقات متعددة مفصولة بفواصل في FRONTEND_URL
    | Production: https://ground-eg.com
    | Development: http://localhost:3000
    |
    */
    'allowed_origins' => array_filter(array_map('trim', explode(',', env('FRONTEND_URL', 'http://localhost:3000')))),
    
    // أو استخدم patterns للـ subdomains
    // 'allowed_origins_patterns' => ['/\.ground-eg\.com$/'],

    /*
    |--------------------------------------------------------------------------
    | أنماط Origins المسموحة
    |--------------------------------------------------------------------------
    */
    'allowed_origins_patterns' => [],

    /*
    |--------------------------------------------------------------------------
    | Headers المسموحة
    |--------------------------------------------------------------------------
    */
    'allowed_headers' => ['*'],

    /*
    |--------------------------------------------------------------------------
    | Headers المكشوفة
    |--------------------------------------------------------------------------
    */
    'exposed_headers' => [],

    /*
    |--------------------------------------------------------------------------
    | وقت التخزين المؤقت (بالثواني)
    |--------------------------------------------------------------------------
    */
    'max_age' => 86400,

    /*
    |--------------------------------------------------------------------------
    | دعم الـ Credentials
    |--------------------------------------------------------------------------
    |
    | false لأننا نستخدم Bearer Token فقط (بدون Cookies/Sessions)
    | withCredentials: false في axios على الـ Frontend
    |
    */
    'supports_credentials' => false,
];
